def insert_visitor(data):
    with get_conn() as conn:
        try:
            return conn.execute_returning_id(
                """
                INSERT INTO visitors
                (plate_number, company, phone, reason, entry_time, source_call_id, raw_payload, created_at)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?)
                """,
                (
                    data["plate_number"],
                    data["company"],
                    data["phone"],
                    data["reason"],
                    data["entry_time"],
                    data.get("source_call_id"),
                    json.dumps(data, ensure_ascii=False),
                    datetime.now().isoformat(timespec="seconds"),
                ),
            )
        except _INTEGRITY_ERRORS as e:
            raise DuplicateCallError(data.get("source_call_id")) from e


def list_visitors(limit=20):
    with get_conn() as conn:
        rows = conn.execute(
            """
            SELECT id, plate_number, company, phone, reason, entry_time, created_at
            FROM visitors ORDER BY id DESC LIMIT ?
            """,
            (limit,),
        ).fetchall()
        return [dict(r) for r in rows]


# ---- 门卫查询 Agent 用：参数化条件查询。字段白名单写死、值全部走占位符，零注入 ----

def _build_filters(
    company=None,
    date=None,
    reason=None,
    plate_number=None,
    phone=None,
    date_from=None,
    date_to=None,
):
    clauses, params = [], []
    if company:
        clauses.append("company LIKE ?")
        params.append(f"%{company}%")
    if reason:
        clauses.append("reason LIKE ?")
        params.append(f"%{reason}%")
    if plate_number:
        clauses.append("plate_number LIKE ?")
        params.append(f"%{plate_number}%")
    if phone:
        clauses.append("phone = ?")
        params.append(phone)
    if date:
        clauses.append("entry_time LIKE ?")
        params.append(f"{date}%")
    if date_from:
        clauses.append("entry_time >= ?")
        params.append(f"{date_from} 00:00:00")
    if date_to:
        clauses.append("entry_time < ?")
        params.append(f"{date_to} 00:00:00")
    where = (" WHERE " + " AND ".join(clauses)) if clauses else ""
    return where, params


def count_visitors(
    company=None,
    date=None,
    reason=None,
    plate_number=None,
    phone=None,
    date_from=None,
    date_to=None,
):
    where, params = _build_filters(company, date, reason, plate_number, phone, date_from, date_to)
    with get_conn() as conn:
        row = conn.execute(f"SELECT COUNT(*) AS n FROM visitors{where}", params).fetchone()
        return row["n"]


def query_visitors(
    company=None,
    date=None,
    reason=None,
    plate_number=None,
    phone=None,
    date_from=None,
    date_to=None,
    limit=10,
):
    limit = max(1, min(int(limit or 10), 50))
    where, params = _build_filters(company, date, reason, plate_number, phone, date_from, date_to)
    with get_conn() as conn:
        rows = conn.execute(
            f"""
            SELECT id, plate_number, company, phone, reason, entry_time
            FROM visitors{where} ORDER BY id DESC LIMIT ?
            """,
            list(params) + [limit],
        ).fetchall()
        return [dict(r) for r in rows]


def group_visitors_by(field, company=None, date=None, reason=None, plate_number=None, phone=None, date_from=None, date_to=None):
    """按公司或事由聚合统计。field 只允许白名单字段，避免动态 SQL 注入。"""
    if field not in ("company", "reason"):
        raise ValueError("group field must be company or reason")
    where, params = _build_filters(company, date, reason, plate_number, phone, date_from, date_to)
    with get_conn() as conn:
        rows = conn.execute(
            f"""
            SELECT {field} AS name, COUNT(*) AS count
            FROM visitors{where}
            GROUP BY {field}
            ORDER BY count DESC, name ASC
            LIMIT 10
            """,
            params,
        ).fetchall()
        return [dict(r) for r in rows]
